#Wylicz wartości dziesiętne następujących liczb zapisanych w różnych systemach liczbowych:

#• 777 (ósemkowo)
print("ósemkowo")
print(0o777)
print(7*8**0+           7*8**1+           7*8**2,"\n")

#• 1011 (binarnie)
print("binarnie")
print(0b1011)
print(1*2**0+    1*2**1+    0*2**2+      1*2**3         ,"\n")

#• FFF (szesnastkowo)
print("szesnastkowo")
print(0xFFF)
print(   15*16**0+     15*16**1+     15*16**2, "\n"     )

#• 123 (piątkowo)
print("piątkowo")
print(3*5**0+    2*5**1+    1*5**2, "\n"   )